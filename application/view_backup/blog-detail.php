                        <h1 class="header_text">Five Signs of Internal Communication Breakdown in The Workplace</h1>
                </div> 


                <div class="text-justify">  
                    <div class="blog-author">
                        <span class="name">By Bhaswati C Mukherjee</span>
                        <span class="date">March 6, 2018, 1510 hrs</span> 
                    </div>
                    <img style="width: 848px;" class="img-responsive" src="<?php echo(base_url() . 'assets/img/' . 'Comm.jpg'); ?>" ><br>
                    <small class="block">Picture Courtesy: Pixabay.com</small>  <br>  
                    <p>Research by Deloitte and Touche Human Capital showed that 95% of CEOs agreed that effective internal communication is important to the success of the organization, but only 22% agreed that they thought it was being delivered effectively.</p>
                    <p>Most business leaders think of effective internal communication as a complementary, which does not affect the business profit & loss.</p>

                    <p>But, a study by SIS International Research has proven it wrong. It showed that as much as $26,041 is wasted by a worker per year due to productivity losses because of ineffective internal communication.</p>

                    <p>So, effective internal communication does impact a business P&L!</p>

                    <p>Right now, you probably think whether you have built an effective internal communication in your business. Look no further, here's the checklist to help you identify whether your business has a solid internal communication.</p>

                    <p>5 signs your business suffers from communication breakdown:
                    </p>
                    <h5 class="sub-sub-title">Your employees don't know the goals they need to achieve</h5>
                    <p>Try this, walk around in your office, and ask any employees you come across with what their goals are for this month or the next three months. If they struggle to answer your question, your company has a communication breakdown.</p>

                    <p>Managers must communicate with their staffs the goals they need to achieve. It should be emphasized enough by communicating it as often as possible to the point they are completely aware of their goals that they direct all their efforts towards achieving them.</p>

                    <p>Research found that when employees understand their overall role in the business, 90% will work towards that success, but the number plummets to 23% if they don't.</p>
                    <h5 class="sub-sub-title">Work is (often) not done properly</h5>
                    <p>There are several possibilities to this:</p>
                    <ul class="custom">
                        <li>Work is not done as expected</li>
                        <li>Work is done twice by different staffs</li>
                        <li>Work is not done at all</li>

                    </ul>
                    <p>All those different cases emanate from lack of communication of the superiors. It can be from the "staff factor", but it's likely the leaders who don't proactively and clearly communicate and ask if the staffs need additional explanations or resources to fulfill the assignments.</p>

                    <h5 class="sub-sub-title">Employees don't get the same information updates</h5>
                    <p>Unless it's confidential, all employees should get the same information at the same time. Your company is having a communication problem when a staff from a certain department just finds out a company-wide announcement from the management, whereas another staff from the same or different department has heard days before.</p>

                    <p>Unsuitable communication tools used by the company can cause such thing to happen. So, instead of helping your employees communicate better, they create hassles for them.</p>

                    <p>For example, if your company requires fast and instant communication, email or messenger is not the right way to go. A walkie talkie app, on the other hand, can be the right solution for your needs in this case.</p>

                    <p>Different needs require different solutions. Make sure your organization is using appropriate communication tools that cover your needs, thus support your business.</p>
                    <h5 class="sub-sub-title">You get (too) many customer complaints</h5>
                    <p>Customer complaints always exist, so long as a business runs. But, if you get customer complaints way more than you usually do, it's an indicator of a communication breakdown in your company.</p>

                    <p>It could be the customer support don't communicate previous complaints to the product development well, or the product development doesn't inform changes on the products to other departments clearly, or any other ways possible.</p>

                    <p>When the internal communication is obscure, it usually impacts the products/service, which in the end will cause a lot of confusion to the customers.</p> 
                    <h5 class="sub-sub-title">You have a high staff turnover</h5>

                    <p>Staff turnover is a complicated and challenging issue faced by almost all companies in the world. It requires a thorough solution as it is very costly to the company. Losing an employee can cost an organization up to 213% of the employee's salary.</p>

                    <p>71% of employees feel managers don't spend enough time explaining goals and plans, which is why: communicate from "employee-centric" point of view and train the managers to talk clearly and consistently about all policies, are two among several suggested proactive approaches in employee retention</p>

                    <p>Don't let the illusion of subpar communication make you fail to realize the internal communication breakdown faced by your organization.</p>

                    <p>As George Bernard Shaw said, "The biggest problem in communication is the illusion that it has taken place."</p>

