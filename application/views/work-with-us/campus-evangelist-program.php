                <p class="campus-paragraph">
                    Campus Evangelist program will provide you with a platform to become our brand's representative. As a campus evangelists you will spread relevant information to amplify the visibility of Skillpromise in your campus
                </p>

                <h4 class="campus-heading">Responsibilities</h4>

                <ul class="trainers-list campus-list">
                    <li>Develop an in-depth knowledge of Skillpromise programs</li>
                    <li>Build a network of students in your college using offline and online strategies</li>
                    <li>Ideate and execute engaging campaigns in your college</li>
                    <li>Work on marketing plans and carry out campaigns to sell Skillpromise programs</li>
                    <li>Educate the college community about the benefits of Skillpromise programs</li>
                    <li>Display Skillpromise product information by displaying posters on notice boards, distributing fliers and sharing them online</li>
                    <li>Organize events like seminars and workshops as per the agreed plan</li>
                    <li>Conduct marketing and promotional activities using social media promotions</li>
                    <li>Interact and share best practices with other Campus Evangelists</li>
                    <li>Be the Single Point of Contact (SPOC) between Skillpromise and student community</li>
                    <li>Persuade the students to take part in events that aim to promote Skillpromise programs</li>
                    <li>Address the queries of students regarding the events, products and promotions of Skillpromise</li>
                    <li>Report project reports & outcomes. Share MIS on a regular bases</li>
                    <li>Analyze marketing activity reports & key performance indicators for future improvements</li>
                    <li>Collect feedback from students to improve marketing strategies</li>
                    <li>Work closely with the placement committee of the college and college union, if any.</li>
                </ul>

                <h4 class="campus-heading">Requirements</h4>

                <ul class="trainers-list campus-list">
                    <li>Enrolled in undergraduate or graduate degree program in any stream. Candidate should be in second, third or fourth year. First year students cannot apply</li>
                    <li>Excellent verbal and written communication skills</li>
                    <li>Excellent research and writing skills</li>
                    <li>Experience with Microsoft Office Suite or Google platforms</li>
                    <li>Time management and critical thinking skills</li>
                    <li>Process Oriented</li>
                    <li>Outgoing personality</li>
                    <li>Strategic Agility</li>
                    <li>Impact & Influence</li>
                    <li>Digital-savvy</li>
                </ul>

